// Getting the square of a number

let getSquare = function(x) {
  return x * x;
}

// get the square of 2
console.log('The square root of 2 is ' + getSquare(2));

console.log('----------');

// get the square of 3
console.log('The square root of 3 is ' + getSquare(3));

console.log('----------');

// get the square of 4
console.log('The square root of 4 is ' + getSquare(4));

